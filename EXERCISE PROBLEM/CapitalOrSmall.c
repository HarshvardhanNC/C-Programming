#include<stdio.h>
int main()
{
    char ch;
    int n;
    printf("ENTER YOUR CHARACTER: \n");
    scanf("%c",&ch);
    "%d",ch;
    n=ch;
    if(n>=65&&n<=90)
    {
        printf("YOUR ENTERED CHARACTER IS IN UPPERCASE");
    }
    else if(n>=97&&n<=122)
    {
        printf("YOUR ENTERED CHARACTER IS IN LOWERCASE");
    }
    return 0;
}